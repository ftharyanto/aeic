<h2 class="text-center">History and Goals</h2>
<h3>Brief History</h3>
<p>The concept of establishing the ASEAN Earthquake Information Centre (AIEC) was initially put forward by Directors of the National Meteorological Services of ASEAN countries during the 13th Meeting of the ASEAN Sub-Committee on Meteorology and Geophysics (SCMG) in August 1990. The AEIC was expected to be capable of disseminating information on big/strong earthquakes as early as possible occurring in the territories of ASEAN countries, and also to provide scientific and technical training of seismologists from members of ASEAN. The 13th Meeting of SCMG also endorsed that the Centre be co-located with the Meteorological and Geophysical Agency of Indonesia. The 40th Meeting of the Committee on Science and Technology held in October 2000 further endorsed the request of SCMG to obtain an approval from the ASC to officially use the name "ASEAN Earthquake Information Centre (AEIC)".</p>

<h3>Goals</h3>
<ol>
    <li>To rapidly disseminate information on earthquakes and their associated events occurring in various parts of ASEAN countries to authorities and public.</li>
    <li>To provide communication tools for NSCs in ASEAN to exchange earthquake data.</li>
    <li>To build an earthquake data bank accessible to NSCs in ASEAN.</li>
    <li>To undertake research and training programs for improving the capability of the member countries to monitor and locate earthquakes, and to understand the earthquake mechanism and tectonic behaviour in the region.</li>
    <li>To undertake collaborative work with other seismological centres in other regions with the aim of accelerating the transfer of knowledge and technology into the region.</li>
</ol>
