<h4>Introduction</h4>
<p>Malaysia is situated on the southern edge of the Eurasian. It is closed to the most two seismically active plate boundaries, the inter plate boundary between the indo-Australian and Eurasian plates on the west and the inter-plate boundary between the Eurasian and Philippines plates on the east. Major Earthquake originating from these plate boundaries have been felt in Malaysia. Several possible active faults have been delineated and local earthquakes in east Malaysia appear to be related to some of them.

    Peninsular Malaysia is classified as a seismically stable area. There has been no known record of local earthquakes so for, except some reservoir-included events over the Kenyir Dam area in Terengganu (Fig.2) between 1984 to 19986. However, the west coast of peninsular Malaysia is affected by tremors originating from large Sumatran earthquakes on the average of 1.5 t 2 tremors a years [1]. The maximum intensity observed so far was V on the modified Mercalli (MM) scale.

