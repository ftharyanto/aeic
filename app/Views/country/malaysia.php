<p>East Malaysia, on the other hand, is classified as moderately active in Seismicity mainly Sabah. It has experienced earthquakes of local origin (Fig.3) with magnitudes of up to 5.8 on the Richter scale. Some of these resulted in some damages on properties [3]. The maximum intensity observed so far was VII on the MM scale. In addition to the local earthquakes, east Malaysia is also affected by tremors originating from large earthquakes located over southern Philippines and in the Straits of Macassar, Sulu Sea and Celebes Sea.

    It is important to be noted that earthquakes do not need to be of large magnitude to produce severe damage, because the degree of damage depends not only on the physical size of an earthquake but also on other factors such as where and when an earthquakes occurred, the population density in the area concerned and secondary events such as fire.</p>
<h4>Seismic network</h4>
<p>In the late sixties, the government of Malaysia decided to monitor seismic actives in and around the country systematically to meet increasing demand for seismological information. In order to achieve this purpose., the seismological division was established in 1974 at Malaysian Meteorological Services MMS)

    Instrumental recording of seismic event in Malaysia began only 1975 with the establishment of the first seismological station at Petaling Jaya (KLM) under the seismological programs for South East Asia sponsored by UNESCO. At the moment , MMS operates a total of ten seismological station throughout the country. Five in peninsular Malaysia, namely Petaling Jaya (KLM) , Kuala Lumpur (FRM), Ipoh (IPM), Kluang (KGM), and Kuala Terengganu (KTM), and five in East Malaysia, namely Kota Kinabalu (KKM), Kudat (KDM), Tawau (TSM), Bintulu (BTM), and Kuching (KGM).

    Two station (Ipoh and Kota Kinabalu) are equipped with the short period three component seismographs. One station (Bintulu) is equipped with the short period three component seismographs and also three component strong -motion accelerographs. Another seven stations are equipped with the standar short period veritical component seismographs. By end of 1998, MMS would install two more seismological stations in east Malaysia as a continuing effoert to further upgrade its national monitoring network.

    Digital recorders were first introduce in the department in November 1993. At the same time, MMS installed a PC-based Seismic Data Acquisition System SDAS) centered at the headquarter KLM, to link up all the stations by telephone lines. The system would enable seismic data at the seismological stations to be retrieved at pre-programmed times or whenever necessary.

    MMS server as the national information center for seismology in the country. Apart from MMS, other agencies also installed seismograph and accelerographs at the dam sites and others facilities. However reports on their performance are not available.</p>


