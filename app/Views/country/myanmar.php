        <h4>Myanmar</h4>
        <p>Myanmar covers an area of 678,528 km2. The country lies between about 10oN and 28o30 'N and 92o 30'E and 101o 30' E. The greatest north-south extent is about 2,200 km and the greatest east-west extent about 950 km. In the 800 km long part of southern Myanmar, the Taninthayi area, the east west extent is only 50 to 150 km.

            In the west, the Union of Myanmar is bordered by Bangladesh, in the north-west by India, in the north and north-east by the Peoples' Republic of China, in the east by Laos and in the south east by Thailand. The total length of the country's border is 4,000 km. The central and southern part of the country is bounded in the west by the 2,100 km long Rakhine and Taninthayi coastline of the Bay of Bengal and the Andaman Sea.

            Myanmar is the homeland of the great number of different ethnic and linguistic groups. The Union of Myanmar includes 7 federal states, namely Kayin, Mon, Kayah, Rakhine, Chin, Shan and Kachin, and 7 divisions, namely Yangon, bago, Mandalay, Sagaing, Ayeyarwady, Taninthayi, and Magway. The locations of the state and divisions are shown in Figure 1.
        </p>

        <h4>Geological Aspect</h4>
        <p>Myanmar is part of the extra-peninsular region, which also comprises the young fold mountains of the Himalayan and hill ranges of Afghanistan and Baluchistan in the west. All this area forms part of a weak and flexible zone of the earth's crust which has not yet attains its final state of equilibrium. That those folding movements have not yet ceased is proved by the frequent occurrence of earthquakes. Indeed, minor quakes are felt not infrequently in Yangon, the capital city of Myanmar. These may be associated with the most continuous folding of the ridge on the southern end of which stand the Shwedagon pagoda, golden gigantic religious edifice of Buddhism.

            Volcanoes tend to act as safety values and to relieve the strain and pressure; it is to be noted that a number of extinct volcanoes, Mount Popa which situates in the central part of the country is the tourist's attraction for its scenic beauty and a panoramic view of the surrounding countryside.
        </p>

        <h4>Seismicity</h4>
