            <h4>DMH</h4>
            <p>The Department of Meteorology and Hydrology (DMH) is a state organization under the Ministry of Communications, Post and Telegraph of the Government of the Union of Myanmar responsible for undertaking measurements, data management, computations, studies and provisions of services on all aspects of meteorology, hydrology, seismology and agro meteorology to meet the requirements of the nation and its people.
            </p>
            <h4>Seismological Division</h4>
            <p>A full-fledge seismological division was establish in 1986 under the control and management of the Department of Meteorology and Hydrology in accordance with the UNESCO / UNDP Regional project RAS/79/116: Regional Seismological Network of Himalayan Range which unfortunately failed to come up to its implementation stage. Nowadays, the department has four seismological stations and the seismological division is manned by twenty-five staff including three officered.
                The recording of earthquakes, analysis, processing and compilation of seismic data were systematically carried by the division. Seismicity of different regions in the country have also been studied and prepared. All major construction groups in the country had now sought for earthquake information at their construction sites for the purposes of taking earthquake resistant measures. Seismic data for that purposes are also provided and supplied by the department.
            </p>
            <h4>Network</h4>
            <p>The threat of earthquake in Myanmar ha been long recognized by the government and its people and efforts, therefore, were being made to establish a seismological network to deal with all works pertaining to seismicity ever since the early sixties. Earthquake recording was undertaken at Yangon as early as 1962 and at Mandalay in 1966, using only then the electromagnetic seismographs with photographic recordings.

                In September 1970, a moderate earthquake with its epicenter near Yangon, there were many buildings suffered cracks and minor damage. A team of Japanese seismological experts was invited by the government to find out what could be done with the threat of earthquakes in Myanmar. The team, in addition to its recommendation for seismic resistant measures in construction and the formulation of earthquake resistant regulations, also recommended the seismological network consisting of 8 stations to provide a full coverage of earthquakes in and near Myanmar, whenever only earthquakes of 5.0 and above on Richter scale occurred. Those stations should be located at Yangon, Mandalay, Sittway, Dawei, Myitkyina, Kalemyo, Magway and Taunggyi.

                Another moderate earthquake jolted central Myanmar in 1975 with its epicenter near Pagan - Nyaung Oo and produced slight to moderate damages to many religious edifices and brick buildings. This Pagan earthquake had awakened the country to the fact that earthquake threat to the country is very dangerous to its people and properties. In 1976, two sets of Japanese visual velocity type seismograph were purchased and each set was installed at Yangon and Mandalay in 1977. In 1984, two sets of solar powered visual velocity type seismograph were obtained through the assistance was installed at Sittway in 1984 and another was installed at Dawei in 1985.

                A digitized seismograph, manufactured in USA was obtained though the assistance of the regional UNESCO office in New Delhi, India in 1995. The relevant software and training on interpretation of seismic are being request to the UNESCO.
            </p>
            <h4>Monitoring</h4>
            <p>There are only four seismological stations for monitoring earthquakes in and near the country. Out of four stations, three stations at Mandalay, Dawei and Sittway are equipped with Japanese Katsujima short-period seismographs. Only in Yangon, capital city of Myanmar, one Japanese Katsujima short-period seismograph, one Japanese Katsujima long-period seismograph and one US K2 Digitized Seismograph, altogether three seismograph are installed. Locations of the existing and planned seismological stations are shown in Figure 2.

                Whenever an earthquake occurred in the country, seismic data were collected from the out-stations simultaneously by using telephones, single side band (S.S.B) etc. Then the locations of epicenter with body wave magnitude and origin time was analyzed and determined at Yangon, main seismological office. After thoroughly analyzing the seismic data, earthquake information was disseminated to the people and the users through radio, television and news papers.

                Since our network is not completed yet, in case of emergency to locate the epicenters of earthquake which had occurred in the country, we have to make, sometimes, request the neighboring ASEAN countries for the earthquake information by using fax.

                Altough our Japanese analog seismographs are old and still in urgent need of calibration, repair and replacement, the seismological division is doing its best to run all the existing stations smoothly and to provide the country with seismological data for national projects and construction work.

                As engineers require modern design technology and basic seismic information, preferably for long period, to construct earthquake resistant buildings and infrastructure, seismic zoning of the country for designing is necessary for safety and efficiency. For this purpose, the department provide the seismicity index map which will in some way serve as zoning map for earthquake risk. The seismicity index map is illustrated in Figure 3.</p>
            <h4>Conclusion</h4>
            <p>Myanmar tries its best to upgrade the monitoring system with the intention to lessen and mitigate the disastrous effects of earthquakes. It is envisaged that when the complete seismological network recommended by the Japanese seismological experts could be established and well-equipped with modernized instrument, disasters due to earthquakes in this part of the world could be thoroughly understood and more efficient steps be taken to combat against their disastrous effects.
            </p>
