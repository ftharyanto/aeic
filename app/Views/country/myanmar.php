            A geological collision between Indian sub continental plate and the rest of Eurasia Continental plate on which Myanmar stands and the collision is still in progress. The world highest mountains, Himalayas rise abruptly from the flat, densely populate Ganges Plain in the northern India and shield the Tibatan plateau from the seasonally shifting monsoon winds of southern Asia.

